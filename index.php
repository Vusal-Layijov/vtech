<script>
    document.addEventListener('scroll', () => {
        const nav = document.querySelector('.navbar');
        if (window.scrollY > 40) { nav.classList.add('scrolled'); } else { nav.classList.remove('scrolled'); }
    });

    // Set min date to today
    const dateInput = document.getElementById('preferred_date');
    if (dateInput) {
        const today = new Date().toISOString().split('T')[0];
        dateInput.min = today;
    }

    // Package buttons auto-select service
    document.querySelectorAll('.book-package').forEach(btn => {
        btn.addEventListener('click', () => {
            const serviceSelect = document.getElementById('service');
            if (serviceSelect) {
                serviceSelect.value = btn.dataset.service;
                serviceSelect.dispatchEvent(new Event('change'));
            }
            document.getElementById('contact').scrollIntoView({ behavior: 'smooth' });
        });
    });

    const form = document.getElementById('bookingForm');
    const statusEl = document.getElementById('formStatus');
    if (form) {
        form.addEventListener('submit', async (e) => {
            e.preventDefault();
            statusEl.textContent = 'Sending...';
            const formData = new FormData(form);
            try {
                const response = await fetch('send_booking.php', { method: 'POST', body: formData });
                const result = await response.json();
                if (result.success) {
                    statusEl.textContent = result.message;
                    statusEl.classList.remove('text-danger');
                    statusEl.classList.add('text-success');
                    form.reset();
                } else {
                    statusEl.textContent = result.message || 'Something went wrong. Please call us.';
                    statusEl.classList.add('text-danger');
                }
            } catch (error) {
                statusEl.textContent = 'Could not send. Please call (412) 583-9593.';
                statusEl.classList.add('text-danger');
            }
        });
    }
</script>
